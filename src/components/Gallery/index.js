class Gallery extends Component {
  state = {
    activeOne: imagesList[0].id,
  }

  updatedImgUrl = id => {
    this.setState({activeOne: id})
  }

  getFilteredImage = () => {
    const {activeOne} = this.state
    const filteredImage = imagesList.filter(each => each.id === activeOne)
    return filteredImage
  }

  render() {
    const {activeOne} = this.state
    const filteredImage = this.getFilteredImage()
    return (
      <div>
        <div className="container">
          {filteredImage.map(each => (
            <img
              src={each.imageUrl}
              className="img"
              alt={each.imageAltText}
              key={each.id}
            />
          ))}
        </div>

        <h1>Nature Photography</h1>
        <p>Nature Photography by Rahul</p>
        <div>
          <ul>
            {imagesList.map(each => (
              <li key={each.id}>
                <ThumbnailItem
                  details={each}
                  isActive={each.id === activeOne}
                  updatedImgUrl={this.updatedImgUrl}
                />
              </li>
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
